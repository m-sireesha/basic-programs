
class Sireesha:
    
    @staticmethod
    def add(x,y):
        print("sum:",x+y)
        
    @staticmethod
    def product(x,y):
        print("product",x*y)
        
    @staticmethod
    def average(x,y):
        print("average",(x+y)/2)

Sireesha.add(10,20)
Sireesha.product(10,20)
Sireesha.average(10,20)